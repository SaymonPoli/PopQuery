INSERT INTO Ingresso (id_ingresso, valor, meia_entrada, fk_id_sessao)
SELECT
    ROW_NUMBER() OVER () AS id_ingresso,
    CASE
        WHEN (ROW_NUMBER() OVER () % 3 = 0) THEN 15.00  -- meia-entrada
        ELSE 30.00  -- inteira
    END AS valor,
    CASE
        WHEN (ROW_NUMBER() OVER () % 3 = 0) THEN TRUE   -- meia-entrada
        ELSE FALSE
    END AS meia_entrada,
    s.id_sessao
FROM Sessao s
CROSS JOIN Assento a;
---------------------------------------------------------
-- CLIENTE_INGRESSO (VENDAS)
---------------------------------------------------------
INSERT INTO Cliente_Ingresso (fk_id_cliente, fk_id_ingresso, quantidade, valor_total, data_emissao, id_nota)
SELECT
    (FLOOR(random()*100)+1)::int AS fk_id_cliente,      -- Clientes de 1 a 100
    i.id_ingresso,
    1 AS quantidade,
    i.valor AS valor_total,
    s.data AS data_emissao,                             -- Data igual à data da sessão
    (FLOOR(random()*1000)+10001)::int AS id_nota        -- Nota fiscal entre 10001 e 11000
FROM Ingresso i
JOIN Sessao s ON i.fk_id_sessao = s.id_sessao
ORDER BY random()
LIMIT 150000;
